// - Alta cohesión significa que la clase se enfoca en lo que deberia estar haciendo, es decir,
// solo métodos relacionados con la intencion de la clase

//Acoplamiento
// Se refiere a cuán relacionadas o dependientes son dos clases o módulos entre sí
// Bajo acoplamiento: cambiar algo importante en una clase no deberia afectar a la otra

// Alto acoplamiento: Dificultaria el cambio y el mantenimiento de su código; dado que las clases están
// muy unidas, hacer un cambio podria requerir una renovacion completa del sistema.